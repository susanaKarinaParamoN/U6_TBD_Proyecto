    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed
        
        
        model.setRowCount(0);
        modeltar.setRowCount(0);
        if (cbxOpciones.getSelectedItem().equals("Cambiar Contrase√±as")) {
            pnlDirecciones.setVisible(false);
            pnlPass.setVisible(true);
            pnlTarjetas.setVisible(false);
        } else if (cbxOpciones.getSelectedItem().equals("Mis pagos")) {
            pnlDirecciones.setVisible(false);
            pnlPass.setVisible(false);
            pnlTarjetas.setVisible(true);
   
        } else if (cbxOpciones.getSelectedItem().equals("Administrar direcciones")) {
            pnlDirecciones.setVisible(true);
            pnlPass.setVisible(false);
            pnlTarjetas.setVisible(false);
        } else if (cbxOpciones.getSelectedItem().equals("Pedidos")) {
            pnlDirecciones.setVisible(false);
            pnlPass.setVisible(false);
            pnlTarjetas.setVisible(false);
            Pedidos objPedidos = new Pedidos(datos2);
            objPedidos.setVisible(true);
            this.setVisible(false);
        }

        if (!dir1.equals("")) {
            model.addRow(new Object[]{dir1});

        }
        if (!dir2.equals("")) {
            model.addRow(new Object[]{dir2});
        }
        try {
            if (!tar.equals("")) {
            modeltar.addRow(new Object[]{tar});
        } 
        } catch (Exception e) {
            System.out.println("error");
        }
        
    }//GEN-LAST:event_btnContinuarActionPerformed

